# programma che  calcoli l'area e il perimetro
# di una circonferenza leggendo il raggio.

print( " Area del cerchio" )
r= float(input( " inserire un valore per il raggio : "))

perimetro = (r*2*3.14159)
area = 3.14159*(r**2)

print (" il perimetro è di" , perimetro, "cm")
print (" l'area è di" , area, "cm")

